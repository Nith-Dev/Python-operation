# Type Casting = the process of converting datatype to another datatype

name = "Manith"
age = 26
gpa = 3.5
IfStudent = True

print(type(name))
print(type(age))
print(type(gpa))
print(type(IfStudent))

# Casting datatype

age = float(age)
gpa = int(gpa)
print("After Convert:",type(age))
print("After Convert:",type(gpa))

